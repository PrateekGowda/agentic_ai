                    f"- Environment: `{environment}`\n"
                    f"- Region: `{spec['region']}`\n"
                    "- Creates Terraform infrastructure generated specifically for this request.\n"
                    "- Stores project state and logs in the platform S3 state bucket.\n"
                    "- Runs compliance checks before approval and deployment.\n\n"
